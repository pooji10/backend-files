package DAY1;
public class operator{
public static void main(String args[])
{
	int a=10;
	int b=20;
	System.out.println("a==b =" + (a==b));
	System.out.println("a!=b =" + (a!=b));
	System.out.println("a>b =" + (a>b));
	System.out.println("a<b =" + (a<b));
	System.out.println("b>=a =" + (b>=a));
	System.out.println("b<=a =" + (b<=a));
	System.out.println((a==b)&&(a<b));
	System.out.println((a<b)||(a>b));
	System.out.println(a&b);
	System.out.println(5&9);
	System.out.println(5|9);
	System.out.println(5^9);
}

}
